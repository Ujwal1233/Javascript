var date=new Date()
console.log(date)

//Methods of date object
//1.getDate() method is used to get the current date
var currentDate=date.getDate()
console.log(currentDate)

//2.getMonth() method is used to get the current month
//note month always starts from 0
var month=date.getMonth()
console.log(month)

//3.getDay() method is used to get the current day
var day=date.getDay()
console.log(day)

//4.getFullYear() method is used to get the current year
var year=date.getFullYear()
console.log(year)

//5.getHours() method is used to get the current hour
var hour=date.getHours()
console.log(hour)

//6.getMinutes() method is used to get the current minutes
var minute=date.getMinutes()
console.log(minute)

//7.getSeconds() method is used to get the current seconds
var second=date.getSeconds()
console.log(second)


